tekrar:

MOV BX,[120h]  ; CARPANİN 16-0 BİTİNİ DIYA AKTAR
AND BX,01H 	; CARPANİN LSB'SI DIŞINDA DİĞER BİTLERİ 0LA ANDLE YANİ 0 YAP (LSBYİ 1 LE ANDLE YANİ LSB ONCEDEN NEYSE SONRADAN DA O OLACAK)
XOR BX,01H	; CARPANİN LSB BİTİ 0 MI DİYE KONTROL ET 0 SA ZERO FLAG 0  A EŞİTLENİR
JZ topla_kaydir ; CARPANİN LSB BİTİ 1 İSE ZERO FLAG 1 E EŞİTLENİR VE JZ DEKİ FONKSİYONA GİT
CLC	; CARPANİN LSB BİTİ 1 DEGİLDİR ZERO FLAG 0 DIR CARRYİ TEMİZLE


devam:

MOV AX,[0136h]
RCR AX,1        ;CARPIM SONUCUNUN 64-48 BİTİNİ BİR SAGA KAYDIR
MOV [0136h],AX
MOV BX,[0134h]      
RCR BX,1        ;CARPIM SONUCUNUN 48-32 BİTİNİ BİR SAGA KAYDIR
MOV [0134h],BX
MOV CX,[0132h]
RCR CX,1        ;CARPIM SONUCUNUN 32-16 BİTİNİ BİR SAGA KAYDIR
MOV [0132h],CX	
MOV DX,[0130h]
RCR DX,1        ;CARPIM SONUCUNUN 16-0  BİTİNİ BİR SAGA KAYDIR
MOV [0130h],DX	  
MOV AX,[0122h]
SHR AX,1        ;CARPANİ BİR BİT SAGA KAYDIR
MOV [0122h],AX 
MOV BX,[0120h]
RCR BX,1        ;CARPANİ BİR BİT SAGA KAYDIR AMA CARRY İLE BİRLİKTE GELİRSE YANİ CARPANİN 32-16 BİTİNDEN GELEN ELDEYİ DE KULLANARAK
MOV [0120h],BX 
   		
DEC SI		;DONGU DEGİSKENİNİ AZALT
CMP SI,0	;DEGİSKEN 0 MI DİYE BAK	
JNZ tekrar	;SIFIR DEGİ, KONTROLCUYE GİT
JMP son		;UYGULAMAYI BİTİRMEYE GİT 


topla_kaydir: 

