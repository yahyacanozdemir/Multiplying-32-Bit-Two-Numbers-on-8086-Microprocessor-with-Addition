MOV DX,[0126h]  ;DX'E CARPILANIN 32-16 BITINI KOPYALA
ADC [0136h],DX  ;CARPIMIN 64-48 BITINE CARRY I GOZ ONUNE ALARAK ATA
JMP devam  


