MOV WORD PTR [0134h],0000h  ;CARPIMIN 48-32 arasinin bellek adresi
MOV WORD PTR [0136h],0000h  ;CARPIMIN 64-48 arasinin bellek adresi

SUB AX,AX
SUB BX,BX
SUB CX,CX
SUB DX,DX
SUB SI,SI
SUB DI,DI


